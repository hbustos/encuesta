<h1>Acerca de</h1>

<p>El presente proyecto de grado trata de la Política de los Sistemas de Gestión de la Seguridad de la Información S.G.S.I. como herramienta para la el aumento de la competitividad y confianza de las medianas y grandes empresas de habla hispana. </p>

<p>El objetivo del general del proyecto es desarrollar un software que ayude a la construcción del documento que contiene la Política del S.G.S.I., y que posiblemente acompañado de un servicio de asesorías podría ser la combinación perfecta 
para el inicio de los procesos de estandarización con la finalidad de alcanzar certificaciones de carácter mundial como las que expide la Organización de Estándares Internacionales ISO.</p>

<p>Específicamente realizaremos un levantamiento de requerimientos a través de una encuesta, la cual cuantificaremos y de ella estableceremos las necesidades de nuestro segmento principal, que está compuesto por todo el personal de TI, 
y en especial el encargado de la Seguridad de la Información, siendo este el insumo principal para la elaboración de un software a medida.</p>
